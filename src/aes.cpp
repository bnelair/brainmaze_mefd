void AES128::key_expansion(const char* password, ui1* expanded_key) {
    ui1 key[16] = {0};
    std::strncpy(reinterpret_cast<char*>(key), password, 16);
    
    // First round key is the key itself
    for (int i = 0; i < NK; ++i) {
        expanded_key[i * 4] = key[i * 4];
        expanded_key[i * 4 + 1] = key[i * 4 + 1];
        expanded_key[i * 4 + 2] = key[i * 4 + 2];
        expanded_key[i * 4 + 3] = key[i * 4 + 3];
    }
    
    // All other round keys
    int i = NK;
    while (i < (NB * (NR + 1))) {
        ui1 temp[4];
        for (int j = 0; j < 4; ++j) {
            temp[j] = expanded_key[(i - 1) * 4 + j];
        }
        
        if (i % NK == 0) {
            // RotWord
            ui1 k = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = k;
            
            // SubWord
            temp[0] = s_sbox[temp[0]];
            temp[1] = s_sbox[temp[1]];
            temp[2] = s_sbox[temp[2]];
            temp[3] = s_sbox[temp[3]];
            
            temp[0] ^= s_rcon[i / NK];
        }
        
        expanded_key[i * 4] = expanded_key[(i - NK) * 4] ^ temp[0];
        expanded_key[i * 4 + 1] = expanded_key[(i - NK) * 4 + 1] ^ temp[1];
        expanded_key[i * 4 + 2] = expanded_key[(i - NK) * 4 + 2] ^ temp[2];
        expanded_key[i * 4 + 3] = expanded_key[(i - NK) * 4 + 3] ^ temp[3];
        
        ++i;
    }
}

void AES128::encrypt(const ui1* input, ui1* output, const char* password) {
    ui1 expanded_key[EXPANDED_KEY_SIZE];
    key_expansion(password, expanded_key);
    cipher(input, output, expanded_key);
}

void AES128::encrypt_with_key(const ui1* input, ui1* output, const ui1* expanded_key) {
    cipher(input, output, expanded_key);
}

void AES128::decrypt(const ui1* input, ui1* output, const char* password) {
    ui1 expanded_key[EXPANDED_KEY_SIZE];
    key_expansion(password, expanded_key);
    inv_cipher(input, output, expanded_key);
}

void AES128::decrypt_with_key(const ui1* input, ui1* output, const ui1* expanded_key) {
    inv_cipher(input, output, expanded_key);
}

void AES128::add_round_key(int round, ui1 state[4][4], const ui1* round_key) {
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[j][i] ^= round_key[round * NB * 4 + i * NB + j];
        }
    }
}

void AES128::sub_bytes(ui1 state[4][4]) {
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[i][j] = s_sbox[state[i][j]];
        }
    }
}

void AES128::inv_sub_bytes(ui1 state[4][4]) {
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[i][j] = s_rsbox[state[i][j]];
        }
    }
}

void AES128::shift_rows(ui1 state[4][4]) {
    ui1 temp;
    
    // Row 1: shift left by 1
    temp = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = temp;
    
    // Row 2: shift left by 2
    temp = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;
    
    // Row 3: shift left by 3
    temp = state[3][3];
    state[3][3] = state[3][2];
    state[3][2] = state[3][1];
    state[3][1] = state[3][0];
    state[3][0] = temp;
}

void AES128::inv_shift_rows(ui1 state[4][4]) {
    ui1 temp;
    
    // Row 1: shift right by 1
    temp = state[1][3];
    state[1][3] = state[1][2];
    state[1][2] = state[1][1];
    state[1][1] = state[1][0];
    state[1][0] = temp;
    
    // Row 2: shift right by 2
    temp = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;
    
    // Row 3: shift right by 3
    temp = state[3][0];
    state[3][0] = state[3][1];
    state[3][1] = state[3][2];
    state[3][2] = state[3][3];
    state[3][3] = temp;
}

inline ui1 AES128::multiply(ui1 x, ui1 y) {
    return ((y & 1) * x) ^
           ((y >> 1 & 1) * xtime(x)) ^
           ((y >> 2 & 1) * xtime(xtime(x))) ^
           ((y >> 3 & 1) * xtime(xtime(xtime(x)))) ^
           ((y >> 4 & 1) * xtime(xtime(xtime(xtime(x)))));
}

void AES128::mix_columns(ui1 state[4][4]) {
    for (int i = 0; i < 4; ++i) {
        ui1 a = state[0][i];
        ui1 b = state[1][i];
        ui1 c = state[2][i];
        ui1 d = state[3][i];
        
        state[0][i] = multiply(a, 0x02) ^ multiply(b, 0x03) ^ c ^ d;
        state[1][i] = a ^ multiply(b, 0x02) ^ multiply(c, 0x03) ^ d;
        state[2][i] = a ^ b ^ multiply(c, 0x02) ^ multiply(d, 0x03);
        state[3][i] = multiply(a, 0x03) ^ b ^ c ^ multiply(d, 0x02);
    }
}

void AES128::inv_mix_columns(ui1 state[4][4]) {
    for (int i = 0; i < 4; ++i) {
        ui1 a = state[0][i];
        ui1 b = state[1][i];
        ui1 c = state[2][i];
        ui1 d = state[3][i];
        
        state[0][i] = multiply(a, 0x0e) ^ multiply(b, 0x0b) ^ multiply(c, 0x0d) ^ multiply(d, 0x09);
        state[1][i] = multiply(a, 0x09) ^ multiply(b, 0x0e) ^ multiply(c, 0x0b) ^ multiply(d, 0x0d);
        state[2][i] = multiply(a, 0x0d) ^ multiply(b, 0x09) ^ multiply(c, 0x0e) ^ multiply(d, 0x0b);
        state[3][i] = multiply(a, 0x0b) ^ multiply(b, 0x0d) ^ multiply(c, 0x09) ^ multiply(d, 0x0e);
    }
}

void AES128::cipher(const ui1* input, ui1* output, const ui1* round_key) {
    ui1 state[4][4];
    
    // Copy input to state array
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[j][i] = input[i * 4 + j];
        }
    }
    
    // Add first round key
    add_round_key(0, state, round_key);
    
    // Main rounds
    for (int round = 1; round < NR; ++round) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(round, state, round_key);
    }
    
    // Final round (no MixColumns)
    sub_bytes(state);
    shift_rows(state);
    add_round_key(NR, state, round_key);
    
    // Copy state to output
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            output[i * 4 + j] = state[j][i];
        }
    }
}

void AES128::inv_cipher(const ui1* input, ui1* output, const ui1* round_key) {
    ui1 state[4][4];
    
    // Copy input to state array
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[j][i] = input[i * 4 + j];
        }
    }
    
    // Add last round key
    add_round_key(NR, state, round_key);
    
    // Main rounds
    for (int round = NR - 1; round > 0; --round) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(round, state, round_key);
        inv_mix_columns(state);
    }
    
    // Final round
    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(0, state, round_key);
    
    // Copy state to output
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            output[i * 4 + j] = state[j][i];
        }
    }
}

